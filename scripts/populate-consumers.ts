export async function populateConsumers() {
  console.log('Populating consumers...');
  
  try {
    // Clear existing consumers first
    await db.delete(consumers);
    
    // Insert all consumers
    const insertPromises = consumersList.map(name => 
      db.insert(consumers).values({
        name: name.trim(),
        department: null
      })
    );
    
    await Promise.all(insertPromises);
    
    console.log(`Successfully populated ${consumersList.length} consumers!`);
  } catch (error) {
    console.error('Error populating consumers:', error);
    throw error;
  }
}

